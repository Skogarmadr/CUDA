/*--------------------------------------*\
 |*		Public			*|
 \*-------------------------------------*/

bool isPiOMPforCritical_Ok(int n)
    {
    return isAlgoPI_OK(piOMPforCritique, n, "Pi OMP for critique");
    }

/*--------------------------------------*\
 |*		Private			*|
 \*-------------------------------------*/

/**
 * synchronisation couteuse!
 */
double piOMPforCritique(int n)
    {
    const double Dx = 1 / (double) n;
    double sum = 0;
#pragma omp parallel for

        for (int i = 1; i <= n; i++)
    	{
    	double Xi = i * Dx;
#pragma omp critical
    	{
    	sum += fpi(Xi);
    	}
    	}
        return sum * Dx;
    }

